for (const [file, summary] of Object.entries(summaries)) {
  const filePath = path.join(postsDir, file)
  if (!fs.existsSync(filePath)) {
    console.log(`Skipped (not found): ${file}`)
    continue
  }
  let raw = fs.readFileSync(filePath, 'utf-8')
  raw = raw.replace(/\r\n/g, '\n')

  const escaped = summary.replace(/"/g, '\\"')
  const summaryLine = `summary: "${escaped}"`

  const newRaw = raw.replace(
    /^(---\n)([\s\S]*?\n)(---\n)/,
    (match, openFm, fmBody, closeFm) => {
      fmBody = fmBody.replace(/^summary:.*\n?/m, '')
      fmBody = fmBody.replace(/\s+$/, '') + '\n'
      return openFm + fmBody + summaryLine + '\n' + closeFm
    }
  )

  if (newRaw === raw) {
    console.log(`Failed: ${file}`)
    continue
  }
  fs.writeFileSync(filePath, newRaw, 'utf-8')
  console.log(`Updated: ${file}`)
}

console.log('\nDone!')
